def f(x, y):
  """Returns the square of the sum of x and y."""
  return x**2 + y**2


if __name__ == "__main__":
  print(f(1, 2))  # Prints 5
  print(f(2, 3))  # Prints 13
  print(f(3, 4))  # Prints 25

